
"""
This module performs power operations.
"""


def calculate_power(base: float, exponent: float) -> int:
    """
    Calculate the power of a base number.

    Args:
        base (float): The base number.
        exponent (float): The exponent.

    Returns:
        int: The base raised to the power of the exponent.
    """
    return int(base) ** exponent
